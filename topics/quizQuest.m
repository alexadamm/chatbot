q{
<h3 align="center">Pembahasan</h3>
<p>Identifikasi hal yang sudah diketahui di dalam soal :</p>
<p>V = 12 volt</p>
<p>R<sub>1</sub> = 0,5 ohm</p>
<p>R<sub>2</sub> = 4,5 ohm</p>
<p>R<sub>total</sub> = R<sub>1</sub> + R</sub>2</sub> = 5 ohm</p>
<p>Kemudian dicari nilai kuat arus melalui rumus berikut :</p>
<p><b>I=V/R</b> = 12/(0,5+4,5) = 2,4 A</p>
<br>
<h4 style="font-style:italic;">Kesimpulan,</h4>
<p>Jadi kuat arus aki yang semula bisa menghasilkan 24 Ampere ketika dipasangkan dengan lampu yang hambatannya 4,5 ohm menjadi <b>2,4 Ampere.</b></p>
}q
